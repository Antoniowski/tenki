        /// <summary>
        /// Draw the specified matrix inside the chosen canvas
        /// </summary>
        /// 
        /// <param name="canvas">
        /// chosen canvas
        /// </param>
        /// 
        /// <param name="matrix">
        /// matrix to draw
        /// </param>
        /// 
        /// <returns>
        /// canvas where matrix has been drawn
        /// </returns>
        private Canvas DrawMatrix(Canvas canvas, int[][] matrix)
        {
            for (int row = 0; row < canvas.Height; row++)
            {
                for (int col = 0; col < canvas.Width; col++)
                {
                    if (matrix[row][col] == 1)
                    {
                        canvas.SetPixel(col, row, Color.White);
                    }
                }
            }
            return canvas;
        }
