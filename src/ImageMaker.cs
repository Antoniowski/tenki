        /// <summary>
        /// Method used to draw the weather icon in a specified canvas
        /// </summary>
        /// 
        /// <param name="canvas">
        /// canvas used to draw
        /// </param>
        /// 
        /// <param name="weather">
        /// weather that will be drawn
        /// </param>
        /// 
        /// <param name="isDay">
        /// true if it is day
        /// </param>
        public Canvas Draw(Canvas canvas, Enums.WMOCodes weather, bool isDay)
        {

            switch (weather)
            {
                case Enums.WMOCodes.ClearSky:
                    if (isDay)
                    {
                        return DrawMatrix(canvas, clearSkyMatrix);
                    }
                    else
                    {
                        return DrawMatrix(canvas, clearSkyNightMatrix);
                    }

                case Enums.WMOCodes.MainlyClear:
                    if (isDay)
                    {
                        return DrawMatrix(canvas, mainlyClearMatrix);
                    }
                    else
                    {
                        return DrawMatrix(canvas, mainlyClearNightMatrix);
                    }

                case Enums.WMOCodes.PartlyCloudy:
                    if (isDay)
                    {
                        return DrawMatrix(canvas, partiallyCloud);
                    }
                    else
                    {
                        return DrawMatrix(canvas, partiallyCloudNight);
                    }

                case Enums.WMOCodes.SlightRainShower:
                    if (isDay)
                    {
                        return DrawMatrix(canvas, lightShower);
                    }
                    else
