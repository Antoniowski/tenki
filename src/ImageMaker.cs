        /// <summary>
        /// Method used to draw the weather icon in a specified canvas
        /// </summary>
        /// 
        /// <param name="canvas">
        /// canvas used to draw
        /// </param>
        /// 
        /// <param name="weather">
        /// weather that will be drawn
        /// </param>
        /// 
        /// <param name="isDay">
        /// true if it is day
        /// </param>
        public Canvas Draw(Canvas canvas, Enums.WMOCodes weather, bool isDay)
        {

            switch (weather)
            {
                case Enums.WMOCodes.ClearSky:
                    if (isDay)
                    {
                        return DrawMatrix(canvas, clearSkyMatrix);
                    }
                    else
                    {
                        return DrawMatrix(canvas, clearSkyNightMatrix);
                    }

                case Enums.WMOCodes.MainlyClear:
                    if (isDay)
                    {
                        return DrawMatrix(canvas, mainlyClearMatrix);
                    }
                    else
                    {
                        return DrawMatrix(canvas, mainlyClearNightMatrix);
                    }

                default:
                    return canvas;
            }
        }


        /// <summary>
        /// Draw the specified matrix inside the chosen canvas
        /// </summary>
        /// 
        /// <param name="canvas">
        /// chosen canvas
        /// </param>
        /// 
        /// <param name="matrix">
        /// matrix to draw
        /// </param>
        /// 
        /// <returns>
        /// canvas where matrix has been drawn
        /// </returns>
        private Canvas DrawMatrix(Canvas canvas, int[][] matrix)
        {
            for (int row = 0; row < canvas.Height; row++)
            {
                for (int col = 0; col < canvas.Width; col++)
                {
                    if (matrix[row][col] == 1)
                    {
                        canvas.SetPixel(col, row, Color.White);
                    }
                }
            }
            return canvas;
        }
